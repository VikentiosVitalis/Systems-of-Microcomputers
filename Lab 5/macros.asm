; Character display
PRINTCH MACRO CHAR
	PUSH AX
	PUSH DX
	MOV DL,CHAR
	MOV AH,2
	INT 21H
	POP DX
	POP AX
ENDM

;Display of string
PRINTSTR MACRO STRING
	PUSH AX
	PUSH DX
	MOV DX,OFFSET STRING
	MOV AH,9
	INT 21H
	POP DX
	POP AX
ENDM

;Change of line
PRINTLN MACRO
	PUSH AX
	PUSH DX
	MOV	DL,13
	MOV	AH,2
	INT	21H
	MOV	DL,10
	MOV	AH,2
	INT	21H
	POP DX
	POP AX
ENDM

